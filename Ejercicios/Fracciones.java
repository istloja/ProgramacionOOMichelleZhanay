package TRABAJO_BIMESTRAL.Fracciones;

/**
 *
 * @author LENOVO
 */
public class Fracciones  {
    private int num;
    private int den;

    public Fracciones(int num, int den) {
        this.num = num;
        this.den = den;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getDen() {
        return den;
    }

    public void setDen(int den) {
        this.den = den;
    }
    public double suma (double f1,double f2){
        double suma = f1+f2;
        return suma;
    }
    public double resta (double f2,double f3){
        
        double resta = f2-f3;
        return resta;
    }

  



    
}

    

